    String Comando = "select * from vendass;";
   
         ResultSet rs;
        
         
         int l = 0;
         try{
             Conexao x = new Conexao();
             rs = x.consultar(Comando);
             
          
             while(rs.next()){
                 jTListagem.setValueAt(rs.getString("codigovenda"), l, 0);
                  jTListagem.setValueAt(rs.getString("nomef"), l, 1);
                   jTListagem.setValueAt(rs.getString("nomec"), l, 2);
                    jTListagem.setValueAt(rs.getString("cpfc"), l, 3);
                     jTListagem.setValueAt(rs.getString("modelo"), l, 4);
                      jTListagem.setValueAt(rs.getString("marca"), l, 5);
                       jTListagem.setValueAt(rs.getString("cor"), l, 6);
                        jTListagem.setValueAt(rs.getString("placa"), l, 7);
                         jTListagem.setValueAt(rs.getString("valor"), l, 9);
                          jTListagem.setValueAt(rs.getString("data"), l, 8);     
                           jTListagem.setValueAt(rs.getString("status"), l,10);                                                                         
                            
                   l++;}
          }catch(SQLException ex){
             Logger.getLogger(ListarVendas.class.getName()).log(Level.SEVERE,null,ex);
         }
    
