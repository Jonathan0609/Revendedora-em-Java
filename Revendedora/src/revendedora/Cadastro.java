  private void limpar(){
    jTnome.setText("");
    jTendereco.setText("");
    jFTFcpf.setValue(null);
    jFTFtelefone.setValue(null);
    jTnome.requestFocus();
  }
  private void cadastro(){
    String N, E, T, C;
    N = jTnome.getText(); 
    C = jFTFcpf.getText();
    E = jTendereco.getText();
    T = jFTFtelefone.getText();
    
    //Pegamos os dados das caixas de textos e guardamos nas variaveis
    
    if(N.length()>=40 || N.length() <=0){
    //Estamos delimitando a quantidade de caracteres escritos no campo
        JOptionPane.showMessageDialog(null,"Nome obrigatorio maximo de 40 caracteres");
        jTnome.requestFocus();
    }else if (T.equals("(  )    -    ")){
        JOptionPane.showMessageDialog(null, "Informe um telefone de contato");
    }else if(E.length()>=40){
        JOptionPane.showMessageDialog(null, "Endere√ßo deve conter no maximo 40 caracteres");
    }else if (C.equals("         -  ")){
        JOptionPane.showMessageDialog(null, "CPF incorreto");
    }else{
        String Comando = "insert into clientes(nome,cpf,endereco,telefone) values"+"('"+N+"','"+C+"','"+E+"','"+T+"');";
        
        
        try{
            Conexao x = new Conexao();
            x.inserir(Comando);
            x.sair();
        }catch(SQLException ex){
            Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE,null,ex);
        }
        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso");
        limpar();
      }
}
